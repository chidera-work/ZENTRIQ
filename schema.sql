-- 5. Security: Public Read Access (Tracking)
DROP POLICY IF EXISTS "Public Read Access Shipments" ON shipments;
CREATE POLICY "Public Read Access Shipments" ON shipments FOR SELECT USING (true);

DROP POLICY IF EXISTS "Public Read Access Updates" ON shipment_updates;
