-- Compatible with Supabase (PostgreSQL) and MySQL (with minor adjustments)

-- 1. Create Shipments Table
CREATE TABLE IF NOT EXISTS shipments (
    id TEXT PRIMARY KEY,
    "trackingNumber" TEXT UNIQUE NOT NULL,
    "senderName" TEXT NOT NULL,
    "senderPhone" TEXT,
    "receiverName" TEXT NOT NULL,
    "receiverPhone" TEXT,
    origin TEXT NOT NULL,
    destination TEXT NOT NULL,
    "currentLocation" TEXT,
    weight TEXT,
    dimensions TEXT,
    "serviceType" TEXT,
    status TEXT DEFAULT 'Pending',
    progress INTEGER DEFAULT 0,
    "estimatedDelivery" TEXT,
    "createdAt" TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Create Shipment Updates Table (Relational History)
CREATE TABLE IF NOT EXISTS shipment_updates (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_id TEXT REFERENCES shipments(id) ON DELETE CASCADE,
    timestamp TEXT NOT NULL,
    location TEXT NOT NULL,
    description TEXT,
    status TEXT NOT NULL
);

-- 3. Create Admins Table
CREATE TABLE IF NOT EXISTS admins (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    email TEXT UNIQUE NOT NULL,
    password TEXT NOT NULL,
    "createdAt" TIMESTAMPTZ DEFAULT NOW()
);

-- 4. Create Password Resets Table
CREATE TABLE IF NOT EXISTS password_resets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT NOT NULL,
    code TEXT NOT NULL,
    expires TIMESTAMPTZ NOT NULL,
    "createdAt" TIMESTAMPTZ DEFAULT NOW()
);

